Exactamente, en R, los comentarios se crean utilizando el símbolo `#`. Todo lo que sigue después del `#` en una línea se considera un comentario y no se ejecutará como código.

En el ejemplo que proporcionaste:

```R
# Esto es un comentario
# Y esto es otro comentario
```

Ambas líneas comienzan con `#`, por lo que todo el texto después de ese símbolo en cada línea se trata como un comentario. Estos comentarios no tienen impacto en la ejecución del programa y están ahí simplemente para agregar explicaciones, notas o cualquier información relevante para el programador. Son útiles para hacer el código más legible y comprensible para quien lo lee, pero no afectan el funcionamiento del código en sí.