En R, los comentarios se introducen utilizando el símbolo `#`, de manera similar a los comentarios en Python. Los comentarios son líneas de texto que no se ejecutan como parte del código, sino que se utilizan para proporcionar explicaciones, aclaraciones o anotaciones para mejorar la comprensión del código por parte de los programadores.

En el código que proporcionaste:

```R
# Los comentarios de R son como los de Python
print("Esto ya no es un comentario")
```

La línea que comienza con `#` es un comentario en R. Todo lo que sigue después del `#` en esa línea no se considera como código ejecutable, por lo que es ignorado por el intérprete de R.

La segunda línea, que contiene `print("Esto ya no es un comentario")`, no está precedida por `#`, por lo tanto, es código activo y se ejecutará. En este caso, el código imprimirá el texto "Esto ya no es un comentario" en la consola utilizando la función `print()`.

Es importante recordar que los comentarios son útiles para hacer que el código sea más legible y comprensible, tanto para uno mismo como para otros programadores que puedan leer o trabajar en el código en el futuro.